# Arithmatic Calculator

num_1 = input('Enter First Number : ')
num_2 = input('Enter Second Number : ')

add= int(num_1) + int(num_2)
sub= int(num_1) - int(num_2)
mul= int(num_1) * int(num_2)
div= int(num_1) / int(num_2)

print(' The Addition = {0} '.format(add))
print(' The Subtraction = {0} '.format(sub))
print(' The Multiplication = {0} '.format(mul))
print(' The Division = {0} '.format(div))